#include "main.h"
#include <stdio.h>
/**
 * main - Prints its name followed by a new line
 * @argc: number of arguments passed to the function
 * @argv: actual arguments
 * Return: Always 0(success)
 */
int main(int argc __attribute__((unused)), char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}
